    void mostrarTiempo(){
        new Timer(0, new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                Date hora = new Date();
        SimpleDateFormat shora = new SimpleDateFormat("hh:mm:ss a");
        HoraLabel.setText(shora.format(hora));
            }
        }).start();
    }

