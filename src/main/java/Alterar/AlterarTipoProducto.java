    private void OpcionesDatosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_OpcionesDatosActionPerformed
        // TODO add your handling code here:
        //InicializarOpciones();
        OpcionElegida = OpcionesDatos.getSelectedItem().toString();

        elegir = 0;

        if(OpcionElegida.equals("Agregar")) elegir = 1;
        if(OpcionElegida.equals("Modificar")) elegir = 2;
        if(OpcionElegida.equals("Eliminar")) elegir = 3;
        switch(elegir){
            case 1: InicializarAgregar();
            OcultarModificar();
            OcultarEliminar();
            break;
            case 2: InicializarModificar();
            OcultarAgregar();
            OcultarEliminar();
            break;
            case 3: InicializarEliminar();
            OcultarAgregar();
            OcultarModificar();
            break;
        }
    }//GEN-LAST:event_OpcionesDatosActionPerformed

    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        // TODO add your handling code here:
        switch(elegir){
            case 1: AgregarTProducto();
            TProductoIDTexto.setText(null);
            NombreTProductoTexto.setText(null);
            break;
            case 2: ModificarTProducto();
            NombreTProductoModificar.setText(null);
            break;
            case 3: EliminarTProducto();
            TProductoIDEliminar.setText(null);
            break;
        }
        
        String [] NuevosTProductoID = ObtenerIDS();
        IndiceTProductoIDModificar.setModel(new javax.swing.DefaultComboBoxModel<>(NuevosTProductoID));

    }//GEN-LAST:event_jButton1MouseClicked

