    private void InicializarAgregar(){
        NombreTProductoTexto.setEnabled(true);
        String TProductoID = null;
        int TProductoIntIDNuevo = 0;
        String TProductoIDNuevo = null;
        
        try {
            TProductoIDTexto.setEnabled(false);
            
            bd = new Conexion();
            ResultSet rs = bd.ObtenerID(17);
            
            rs.next();
            TProductoID = rs.getString(1);
            TProductoIntIDNuevo = Integer.parseInt(TProductoID);
            TProductoIDNuevo = String.valueOf(TProductoIntIDNuevo + 1);
            TProductoIDTexto.setText(TProductoIDNuevo);
        } catch (SQLException ex) {
            Logger.getLogger(AlterarClientes.class.getName()).log(Level.SEVERE, null, ex);
       
        }
    }
    
