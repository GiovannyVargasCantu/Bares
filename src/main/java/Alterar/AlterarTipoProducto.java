    private void AgregarTProducto(){
        TProductoID = TProductoIDTexto.getText();
        NombreTProducto = NombreTProductoTexto.getText();
        
        Query = "('" + NombreTProducto + "');";
        bd = new Conexion();
        bd.AgregarDatos(Query, 17);
        
        JOptionPane.showMessageDialog(null, "Tipo Producto agregado con éxito.");
    }
    
    private void ModificarTProducto(){
        TProductoID = IDModificacion.getText();
        NombreTProducto = NombreTProductoModificar.getText();
        
        Query = "SET NombreTipoProducto= '" + NombreTProducto
                + "' WHERE TipoProductoID = " + TProductoID;
        
        bd = new Conexion();
        bd.ModificarDatos(Query, 17);
        
        JOptionPane.showMessageDialog(null, "Tipo Producto modificado con éxito.");
    }
    
    private void EliminarTProducto() {
        TProductoID = TProductoIDEliminar.getText();
        Query = "DELETE FROM TipoProducto WHERE TipoProductoID =" + TProductoID + ";";
        
        bd = new Conexion();
        bd.EliminarDatos(Query);
        
        JOptionPane.showMessageDialog(null, "Tipo Producto eliminado con éxito.");
    }

